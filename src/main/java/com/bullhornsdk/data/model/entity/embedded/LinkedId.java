package com.bullhornsdk.data.model.entity.embedded;

import com.bullhornsdk.data.model.entity.core.type.AbstractEntity;
import com.fasterxml.jackson.annotation.JsonInclude;
import com.fasterxml.jackson.annotation.JsonProperty;
import com.fasterxml.jackson.annotation.JsonPropertyOrder;

@JsonInclude(JsonInclude.Include.NON_NULL)
@JsonPropertyOrder({ "id" })
public class LinkedId extends AbstractEntity {

	private Integer id;

	public LinkedId() {
		super();
	}

	public LinkedId(Integer id) {
		super();
		this.id = id;
	}

	@JsonProperty("id")
	public Integer getId() {
		return id;
	}

	@JsonProperty("id")
	public void setId(Integer id) {
		this.id = id;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		LinkedId other = (LinkedId) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}

    @Override
    public String toStringNonNull(boolean includeLineBreaks) {
        String lbc = "";
        if(includeLineBreaks)
            lbc = "\n";
        StringBuilder builder = new StringBuilder("{ ");
        if(id != null){
            builder.append("id=");
            builder.append(id);
        }
        builder.append(lbc +"}");
        return builder.toString();
    }

    @Override
    public String toStringNonNull() {
        StringBuilder builder = new StringBuilder("{ ");
        if(id != null){
            builder.append("id=");
            builder.append(id);
        }
        builder.append("}");
        return builder.toString();
    }

	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append(" {\nid=");
		builder.append(id);
		builder.append("\n}");
		return builder.toString();
	}

}
